			<h1>One Liners</h1>
			<section id="articles" class="columns">
  				<div data-title="">
					<h4>Believe</h4>
					<p>As used in the Bible, it generally means more than mental agreement, although it would include that; it is the entrusting of one's case to God, acknowledging personal need and receiving the Lord Jesus as Saviour.</p>
				</div>
				<div data-title=''>
					<h4>Bible</h4>
					<p>Simply means the "book" and refers to the collection of sixty-six books generally accepted by Christians as the written Word of God.</p>
				</div>
				<div data-title=''>
					<h4>Faith</h4>
					<p>An act of the will in which a person believes God and what He has said; it is not blind, but is based upon the evidence provided in the Bible that God has proved Himself to be faithful and is therefore deserving of our trust.</p>
				</div>
				<div data-title=''>
					<h4>God</h4>
					<p>In the Bible, that one true and supreme being who created everything good, who sustains everything, who has set about to rescue those creatures who have rebelled against Him, and who will some day judge everyone in a just way.</p>
				</div>
				<div data-title="">					
					<h4>Gospel</h4>
					<p>An Old English word meaning "good news" and referring to the message of the Bible that, although we are sinners deserving of God's judgment, God offers complete forgiveness to all who turn from their sin and accept His offer of salvation.</p>
				</div>
				<div data-title="">					
					<h4>Holiness, holy</h4>
					<p>From a word meaning to be different or separate, it refers to God being separate from sin and so different from all others; through faith in Christ's work on the cross, and by obedience to God's Word, the Bible, Christians are able to obey the command of God, "Be holy, as I am holy."</p>
				</div>
				<div data-title="">					
					<h4>Justification</h4>
					<p>A judicial act of God by which He is able to declare a sinner right if such a person agrees with God concerning his/her personal guilt and accepts Jesus Christ as his/her substitute in paying the acceptable price for sin; self-justification by "accusing" others or by "excusing" ourselves is futile.</p>
				</div>
				<div data-title="">					
					<h4>Redemption, redeemed</h4>
					<p>That aspect of salvation which refers to the price paid through the death of Christ that makes it possible for a sinner to be "bought back" from the slave market of sin.</p>
				</div>
				<div data-title="">					
					<h4>Repent, repentance</h4>
					<p>Generally a change of mind that leads to a change of life; more specifically, agreeing with God that I am guilty as charged, offering no excuses in my defence. When linked with faith, it is the pathway to personal salvation.</p>
				</div>
				<div data-title="">					
					<h4>Salvation, saved</h4>
					<p>As commonly used, to be saved means to be saved from some danger, as for example to be saved from drowning or saved from a fire; so salvation in the Bible refers to a person being saved from sin, its penalty and/or its negative influence.</p>
				</div>
				<div data-title="">					
					<h4>Sin</h4>
					<p>Everything that falls short of God's perfect standard, whether by thinking, doing or wanting something wrong or by not doing something right.</p>
				</div>
				<div data-title="">					
					<h4>Trinity</h4>
					<p>Although there is only one God, the Bible reveals God as three persons; this "Godhead" or tri-unity is a mystery but can be illustrated by the equation 1x1x1 = 1.</p>
				</div>
			</section>
			
